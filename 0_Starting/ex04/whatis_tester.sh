#!/bin/bash

RED='\033[0;31m'
B_RED='\033[1;31m'
NC='\033[0m'

echo "----------------------------------------------------------------------"
echo -e "${B_RED}CASE 1:${NC} testing whatis.py with ${RED}14${NC}"
echo "----------------------------------------------------------------------"
python3 whatis.py 14
echo

echo "----------------------------------------------------------------------"
echo -e "${B_RED}CASE 2:${NC} testing whatis.py with ${RED}-5${NC}"
echo "----------------------------------------------------------------------"
python3 whatis.py -5
echo

echo "----------------------------------------------------------------------"
echo -e "${B_RED}CASE 3:${NC} testing whatis.py with ${RED}no arguments${NC}"
echo "----------------------------------------------------------------------"
python3 whatis.py
echo

echo "----------------------------------------------------------------------"
echo -e "${B_RED}CASE 4:${NC} testing whatis.py with ${RED}0${NC}"
echo "----------------------------------------------------------------------"
python3 whatis.py 0
echo

echo "----------------------------------------------------------------------"
echo -e "${B_RED}CASE 5:${NC} testing whatis.py with ${RED}\"Hi!\"${NC}"
echo "----------------------------------------------------------------------"
python3 whatis.py Hi!
echo

echo "----------------------------------------------------------------------"
echo -e "${B_RED}CASE 6:${NC} testing whatis.py with ${RED}13 and 5${NC}"
echo "----------------------------------------------------------------------"
python3 whatis.py 13 5
echo
